#include<stdio.h>
int main()
{
    int x,y;
    printf("The value of x : ");
    scanf("%d",&x);
    printf("The value of y : ");
    scanf("%d",&y);

    printf("\nThe addition of x,y is :%d",x+y);
    printf("\nThe Subtraction of x,y is :%d",x-y);
    printf("\nThe Multiplication of x,y is :%d",x*y);
    printf("\nThe Quotient of x,y is :%d",x/y);
    printf("\nThe Reminder of x,y is :%d",x%y);

return 0 ;
}
